    private void createViewTable(List<Tour> tours) {
        ArrayList<Object[]> data = new ArrayList<>();
        try {
            for (int i = 0; i < tours.size(); i++) {
                Object[] row = new Object[]{tours.get(i).getTourCode(), tours.get(i).getRoute(), tours.get(i).getCoach(),
                    tours.get(i).getStartDate(), tours.get(i).getExecuted(), tours.get(i).getDriver(), tours.get(i).getBills().size(), tours.get(i).getPassengers().size()};
                data.add(row);
            }
            Object[][] passengerData = data.toArray(new Object[0][]);
            tableView.setModel(new javax.swing.table.DefaultTableModel(
                    passengerData,
                    new String[]{
                        "Tour Code", "Route", "Coach", "Start Date", "Executed", "Driver", "No of Bills", "No of Passengers"
                    }
            ) {
                boolean[] canEdit = new boolean[]{
                    false, false, false, false, false, false, false, false
                };

                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit[columnIndex];
                }
            });
            tableView.setAutoscrolls(true);
            tableView.setFillsViewportHeight(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


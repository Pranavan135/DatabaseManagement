    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String strID = txtTourCode.getText();
        List<Tour> result = null;
        Integer id = null;
        if (!strID.equals("")) {
            try {
                id = Integer.valueOf(strID);
                if (!validateID(id)) {
                    JOptionPane.showMessageDialog(this, "Device id must contains exactly " + IdLength + " digits.", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Please enter valid tour code", "Warning", JOptionPane.WARNING_MESSAGE);
                txtTourCode.selectAll();
                txtTourCode.requestFocus();
                return;
            }
            result = tourDAO.getTours(id);
        } else {
            result = tourDAO.getAllTours();
        }

        if (result == null || result.size() == 0) {
            JOptionPane.showMessageDialog(this, "No data found", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            createViewTable(result);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tableViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableViewMouseClicked
        if (evt.getClickCount() == 2) {
            int id = Integer.parseInt(tableView.getValueAt(tableView.getSelectedRow(), 0).toString());
            TourEditPanel panel = TourEditPanel.getInstance();
            panel.refresh(tourDAO.getTour(id));
            Container container = this.getParent();
            container.removeAll();
            container.add(panel);
            container.repaint();
            container.validate();
        }
    }//GEN-LAST:event_tableViewMouseClicked

